// Generates src/assets.gen.ts from the spec package (doc 05 §3 prompt
// architecture). Run: pnpm --filter @clay/mutation gen
// A drift test (test/assets.test.ts) fails when specs change without
// regeneration — prompt changes ship like code (doc 07 §4).
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const specs = join(here, "..", "..", "..", "specs");

const read = (p) => readFileSync(join(specs, p), "utf8").replace(/\r\n/g, "\n");

/** Extract a "## heading ..." section (without the heading line itself). */
export function section(text, headingPrefix) {
  const lines = text.split("\n");
  const start = lines.findIndex((l) => l.startsWith(headingPrefix));
  if (start < 0) throw new Error(`missing section '${headingPrefix}'`);
  let end = lines.length;
  for (let i = start + 1; i < lines.length; i++) {
    if (lines[i].startsWith("## ")) { end = i; break; }
  }
  return lines.slice(start + 1, end).join("\n").trim();
}

export function parseExemplar(md) {
  const intent = md.match(/^INTENT: (.+)$/m)?.[1];
  const json = md.match(/```json\s*\n([\s\S]*?)```/)?.[1];
  if (!intent || !json) throw new Error("exemplar missing INTENT or json block");
  return { intent: intent.trim(), plan: JSON.parse(json) };
}

/** Exemplar 10's NOTE: the prompt version must declare the clients query. */
export function patchExemplar10(plan) {
  plan.panels[0].declared_queries.push({
    from: "clients", select: ["id", "name", "dog_name", "phone"],
  });
  return plan;
}

export function buildAssets() {
  const systemV1 = read("prompt/system-v1.md");
  const doc03 = read("docs/03-kernel-api.md");
  const doc04 = read("docs/04-data-model.md");
  const doc05 = read("docs/05-mutation-pipeline.md");
  const doc11 = read("docs/11-gap-resolutions.md");

  const files = [
    "01-add-field.md", "02-enum-status.md", "03-computed-and-strip.md",
    "04-chart.md", "05-form.md", "06-filter-event-pair.md", "07-clarify.md",
    "08-rename.md", "09-remove.md", "10-out-of-scope.md",
    "11-board.md", "12-cards.md", "13-timeline.md", "14-flow.md", "15-calendar.md",
  ];
  const exemplars = files.map((f, i) => {
    const e = parseExemplar(read(join("exemplars", f)));
    if (i === 9) patchExemplar10(e.plan);
    return { intent: e.intent, plan: JSON.stringify(e.plan, null, 1) };
  });

  return {
    ROLE: section(systemV1, "## §1 Role"),
    HARD_RULES: section(systemV1, "## §5 Hard rules"),
    API_REFERENCE: doc03.trim(),
    MIGRATION_VOCAB: section(doc04, "## 4. Migration vocabulary"),
    OUTPUT_CONTRACT: section(doc05, "## 2. MutationPlan JSON schema"),
    COMPONENT_CONTRACTS: section(doc11, "## G25"),
    EXEMPLARS: exemplars,
  };
}

function main() {
  const a = buildAssets();
  const out = `// GENERATED by scripts/gen-assets.mjs — do not edit by hand.
// Source of truth: the specs/ package. Regenerate with:
//   pnpm --filter @clay/mutation gen
/* eslint-disable */
export const ROLE = ${JSON.stringify(a.ROLE)};
export const API_REFERENCE = ${JSON.stringify(a.API_REFERENCE)};
export const MIGRATION_VOCAB = ${JSON.stringify(a.MIGRATION_VOCAB)};
export const OUTPUT_CONTRACT = ${JSON.stringify(a.OUTPUT_CONTRACT)};
export const COMPONENT_CONTRACTS = ${JSON.stringify(a.COMPONENT_CONTRACTS)};
export const HARD_RULES = ${JSON.stringify(a.HARD_RULES)};
export const EXEMPLARS: readonly { intent: string; plan: string }[] = ${JSON.stringify(a.EXEMPLARS, null, 2)};
`;
  writeFileSync(join(here, "..", "src", "assets.gen.ts"), out);
  console.log("wrote src/assets.gen.ts");
}

if (process.argv[1] === fileURLToPath(import.meta.url)) main();
